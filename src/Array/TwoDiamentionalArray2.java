package Array;


public class TwoDiamentionalArray2 {

	public static void main(String[] args) {
		int ar[][]= {{10,20,30},{40,50,60},{70,80,90}};
		System.out.println("length of the array: "+ar.length);
		
		for (int i[]:ar)
		{
			for (int j:i)
			{
				System.out.print(j+" ");
			}
			System.out.println();
		
		
		
		
		
	}

}
	
}
