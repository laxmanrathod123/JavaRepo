package Loops;

public class Loops {
	
 public static void main (String []args) {
	 int i=0;                 // initialization part
	 while(i<=10) {           // conditional part
		 System.out.println(i);
		 //i=i+1;
		 i++;                 // Increment or decrement part
	 }
	 
	 System.out.println();
	 
	 for(int j=1 ; j<=10; j++) {
		 System.out.println(j);
	 }
	 
System.out.println();
	 
	 for(int k=10 ; k>=-10; k--) {
		 System.out.println(k);
	 }
	 
	 System.out.println();
	for(int counter=0; counter<3; counter++) {
		System.out.println("Hellow world");
	}
	 System.out.println();
	
	// do while loop 
	 int o=12;
	do {
		System.out.println("Apana college");
		}while(o<11);
 }
 }
